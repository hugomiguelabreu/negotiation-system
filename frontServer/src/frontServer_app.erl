-module(frontServer_app).

%% Imports
-include ("autentication.hrl").
-include ("db.hrl").
-include ("getJson.hrl").

%% Application callbacks
-export([start/0, stop/1]).

%%====================================================================
%% API
%%====================================================================

start() ->
	spawn(db, init, []),
	spawn(autentication, startAutentication, []),
	spawn(getJson, start, []),
	spawn(orders, init, []).
    %frontServer_sup:start_link().

%%--------------------------------------------------------------------

stop(_State) ->
    ok.

